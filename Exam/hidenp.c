#include <unistd.h>

int	main(int ac, char **av)
{
	int	i;
	int	j;

	i = 0;
	j = 0;
	if (ac == 3)
	{
		while (av[2][j] && av[1][i])
		{
			if (av[1][i] == av[2][j])
				i++;
			j++;
			if (av[1][i] == '\0')
				return (write (1, "1\n", 2));
		}
		return (write (1, "0\n", 2));
	}
	write (1, "\n", 1);
}
